        public void Draw(SpriteBatch spriteBatch)
        {
            spriteBatch.Begin();

            for (int y = 0; y < 24; y++)
            {
                for (int x = 0; x < 32; x++)
                {
                   // map_array[y, x].Draw();
                    Texture2D tile;
                    switch(map_type_array[y,x])
                    {
                        case 0:
                            {
                                tile = sky;
                                break;
                            }
                        case 1:
                            {
                                tile = sea;
                                break;
                            }
                        case 2:
                            {
                                tile = debris;
                                break;
                            }
                        case 3:
                            {
                                tile = boat;
                                break;
                            }
                        case 4:
                            {
                                tile = oil;
                                break;
                            }
                        case 5:
                            {
                                tile = sea_floor;
                                break;
                            }
                        case 6:
                            {
                                tile = sea_top;
                                break;
                            }
                        default:
                            {
                                tile = sea;
                                break;
                            }
                    }
                    spriteBatch.Draw(tile, new Rectangle(x * 25, y * 25, tile.Width, tile.Height), Color.White);
                }
            }

            spriteBatch.End();
        }

